// 15. let, const 키워드와 블록 레벨 스코프

// 15.1 var 키워드로 선언한 변수의 문제점

// 15.1.1 변수 중복 선언 허용

var x = 1;
var y = 1;
// var 키워드로 선언된 변수는 같은 스코프 내에서 중복 선언을 허용한다.
var x = 100; // 초기화문이 있는 변수 선언은 자바스크립트 엔진에 의해 var 키워드가 없는 것처럼 동작한다.
var y; // 초기화문이 없는 변수 선언문은 무시된다.

console.log(x); // 100
console.log(y); // 1


// 15.1.2 함수 레벨 스코프

// var 키워드로 선언한 변수는 오로지 함수의 코드 블록만을 지역 스코프로 인정한다.
// 따라서 함수 외부에서 var 키워드로 선언한 변수는 코드 블록 내에서 선언해도 모두 전역 변수가 된다.
var x = 1;

if (true) {
  // x는 전역 변수다. 이미 선언된 전역 변수 x가 있으므로 x 변수는 중복 선언된다.
  // 이는 의도치 않게 변수값이 변경되는 부작용을 발생시킨다.
  var x = 10;
}

console.log(x); // 10


// for 문의 변수 선언문에서 var 키워드로 선언한 변수도 전역 변수가 된다.
var i = 10;

// for문에서 선언한 i는 전역 변수다. 이미 선언된 전역 변수 i가 있으므로 중복 선언된다.
for (var i = 0; i < 5; i++) { // 전역 변수 i의 값인 5를 중복선언 해버린다.
  console.log(i); // 0 1 2 3 4
}  

// 의도치 않게 i 변수의 값이 변경되었다.
console.log(i); // 5


// 15.1.3 변수 호이스팅

// 이 시점에는 변수 호이스팅에 의해 이미 foo 변수가 선언되었다. (1. 선언단계)
// 변수 foo는 undefined로 초기화된다 (2. 초기화 단계)
console.log(foo); // undefined

// 변수에 값을 할당(3. 할당 단계)
foo = 123;

console.log(foo); // 123

// 변수 선언은 런타임 이전에 자바스크립트 엔진에 의해 암묵적으로 실행된다.
var foo;


// 15.2 let 키워드

// 15.2.1 변수 중복 선언 금지
var foo = 123;
var foo = 456; // 456

let bar = 123;
let bar = 456; // 선언에러
// 물론 let 키워드는 같은 스코프 내에서만 중복 선언이 불가능하다. 서로 다른 스코프를 가질 시 중복 선언을 허용한다.

// 15.2.2 블록 레벨 스코프

// var의 경우 오직 함수의 코드 블록만을 지역 스코프로 인정하는 함수 레벨 스코프를 따른다.
// 하지만 let의 경우 모든 코드 블록(함수, if문, for문, while문, try/catch문 등)을 지역 스코프로 인정하는 블록 레벨 스코프(block-level scope)를 따른다.

let foo = 1; // 전역 변수

{
  let foo = 2; // 지역 변수
  let bar = 3; // 지역 변수
}

console.log(foo); // 1
console.log(bar); // 선언에러


let x = 1;

{
  let x = 31;
  console.log(x); // 31, 이처럼 let 키워드는 다른 스코프 내에서는 변수를 갱신할 수 있다.
}


// 15.2.3 변수 호이스팅

// var 키워드로 선언한 변수와 달리 leet 키워드로 선언한 변수는 변수 호이스팅이 발생하지 않는 것처럼 동작한다.
console.log(foo); // Error, 만약 var 였다면 undefined가 출력되었을 것이다.
let foo;


//스코프의 시작 지점붜 초기화 시작 지점까지 변수를 참조할 수 없는 구간을 일시적 사각지대(Temporal Dead Zone, TDZ)라고 부른다.

// 런타임 이전에 선언 단계가 실행된다. var 키워드와 달리 아직 변수가 초기화 되지 않았다.
// 초기화 이전의 일시적 사각지대에서는 변수를 참조할 수 없다.
console.log(foo); // 선언에러

let foo; // 변수 선언문에서 초기화 단계가 실행된다.
console.log(foo); // undefined

foo = 1; // 할당문에서 할당 단계가 실행된다.
console.log(foo); // 1


let foo = 1; // 전역 변수

{
  console.log(foo); // 참조에러
  let foo = 2; // 지역 변수
}
// let 키워드도 변수 호이스팅이 발생한다. 따라서 참조에러를 피하기 위해 지역 변수는 최대한 상단에 배치한다.


// 15.2.4 전역 객체와 let

// 엄밀히 말하면 let 키워드로 선언한 전역 변수는 전역 객체의 프로퍼티가 아니다. let 전역 변수는 보이지 않는 개념적인 블록 내에 존재한다.
// 즉, 모든 객체의 최상단에 위치한 window 객체의 프로퍼티로 불렀을 경우 undefined가 반환된다.
var x = 1;
let y = 1;

console.log(window.x); // let 키워드와 달리 1이 출력된다. 즉, var 키워드로 선언한 전역 변수는 진짜 전역 변수이다.
console.log(window.y); // undefined가 출력된다. 즉, let 키워드로 선언한 전역 변수는 가짜 전역 변수이다.


// 15.3 const 키워드

// 15.3.1 선언과 초기화

// const 키워드로 선언한 변수는 반드시 선언과 동시에 초기화 해야한다. 하지 않을시 에러가 발생한다.
`const foo;` // Error


// const 키워드 역시 let 키워드 처럼 변수 호이스팅이 발생하지 않는 것처럼 동작한다.
{ 
  console.log(foo); // 참조에러
  const foo = 1;
  console.log(foo); // 1
}

// 블록 레벨 스코프를 갖는다.
console.log(foo); // 선언에러


// 15.3.2 재할당 금지

// const 키워드로 선언한 변수는 재할당이 금지된다.

const foo = 1;
foo = 2; // 타입에러


// 15.3.2 상수

// 상수는 재할당이 금지된 변수를 말한다. 상수는 상태 유지와 가독성, 유지보수의 편의를 위해 적극적으로 사용해야 한다.

// 세전 가격
{
let preTaxPrice = 100;

// 세후 가격
let afterTaxPrice = preTaxPrice + (preTaxPrice * 0.1); // 0.1의 의미를 명확히 알기 어렵기 때문에 가독성이 좋지 않다.

console.log(afterTaxPrice); // 110
}

// 0.1은 이율을 나타내므로 이를 const 키워드를 사용하여 나타냈다.
{
let preTaxPrice = 100;
const taxRate = 0.1;
// 세후 가격
let afterTaxPrice = preTaxPrice + (preTaxPrice * taxRate);

console.log(afterTaxPrice); // 110
}


// 15.3.4 const 키워드와 객체

// const 키워드로 선언된 변수에 원시 값을 할당한 경우 값을 변경할 수 없다.
// const 키워드로 선언된 변수에 객체를 할당한 경우 값을 변경할 수 있다.
{
const x = 1;

person = {x}

person.x = 4;

console.log(person); // { x: 4 }
}
// const 키워드는 재할당을 금지할 뿐 "불변"을 의미하지는 않는다. 즉, 원시값이 아닌 객체값은 변경할 수 있다.


// 15.4 var, let, const의 사용 권장

// 1) ES6를 사용한다면 var 키워드는 사용하지 않는다.
// 2) 재할당이 필요한 경우에 한정해 let 키워드를 사용한다. 이때 변수의 스코프는 최대한 좁게 만든다.
// 3) 변경이 발생하지 않고 일기 전용으로 사용하는(재할당이 필요 없는 상수) 원시 값과 객체에는 const 키워드를 사용한다. const 키워드는 재할당을 금지하므로 var, let 키워드 보다 안전하다.

